public class Calculadora {
    public int soma(int x, int y) {
        return x + y;
    }

    public int sutracao(int x, int y) {
        return x - y;
    }

    public int multiplicacao(int x, int y) {
        return x * y;
    }

    public double divisao(double x, double y) {
        return x / y;
    }

    public boolean verificarPar(int x) {
        return x % 2 == 0;
    }
}
